#!/usr/bin/python

from collections import deque

def parse_map(map_str):
    return [list(map(int, line)) for line in map_str.strip().split('\n')]

def find_trailheads(grid):
    trailheads = []
    for r in range(len(grid)):
        for c in range(len(grid[0])):
            if grid[r][c] == 0:
                trailheads.append((r, c))
    return trailheads

def bfs(grid, start):
    rows, cols = len(grid), len(grid[0])
                  #Up       Down    Left     Right
    directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]
    queue = deque([start])
    visited = set([start])
    found_nines = set()
    
    while queue:
        r, c = queue.popleft()
        if grid[r][c] == 9:
            found_nines.add((r, c))
        for dr, dc in directions:
            nr, nc = r + dr, c + dc
            if 0 <= nr < rows and 0 <= nc < cols and (nr, nc) not in visited:
                if grid[nr][nc] == grid[r][c] + 1:
                    queue.append((nr, nc))
                    visited.add((nr, nc))
    return len(found_nines)

def calculate_total_score(map_str):
    grid = parse_map(map_str)
    #Fine all the trail heads(0) in the map
    trailheads = find_trailheads(grid)
    total_score = 0
    for trailhead in trailheads:
        #Use breadth first search to see for each trail head how many unique trail ends can be reached
        total_score += bfs(grid, trailhead)
    return total_score

